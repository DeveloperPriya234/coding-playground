//logical operator
//true and true = true
//true and false = false
//false and true = false
//false and false = false

let x = 5;
let y = 10;
console.log( (x < 10) && (y > 5) ); // true && true = true
console.log((x < 10) && (y < 5) ); // true && false = false
console.log((x > 10) && (y > 5) ); // false && true = false
console.log((x > 10) && (y < 5) ); // false && false = false

//logical operator or ||

//true or true = true
//true or false = true
//false or true = true
//false or false = false


console.log( (x < 10) || (y > 5) ); // true || true = true
console.log((x < 10) || (y < 5) ); // true || false = true
console.log((x > 10) || (y > 5) ); // false || true = true
console.log((x > 10) || (y < 5) ); // false || false = false




